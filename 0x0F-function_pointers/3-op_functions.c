#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - addition
 * @a: int
 * @b: int
 * Return: n
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: int
 * @b: int
 * Return: n
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: int
 * @b: int
 * Return: n
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: int
 * @b: int
 * Return: n
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * @a: int
 * @b: int
 * Return: n
 */

int op_mod(int a, int b)
{
	return (a % b);
}
